public class Park {
    public class infoPark {
        private String infoPark;
        private String timeWork;
        private int price;

        public String getInfoPark() {
            return infoPark;
        }

        public void setInfoPark(String infoPark) {
            this.infoPark = infoPark;
        }

        public String getTimeWork() {
            return timeWork;
        }

        public void setTimeWork(String timeWork) {
            this.timeWork = timeWork;
        }

        public int getPrice() {
            return price;
        }

        public void setPrice(int price) {
            this.price = price;
        }
    }
}
